    // PARA ALINEAR LOS DATOS DE LA TABLA
    private void alinear(){
       DefaultTableCellRenderer tcr = new DefaultTableCellRenderer();
       tcr.setHorizontalAlignment(SwingConstants.LEFT);
               
       tablaEmpleado.getColumnModel().getColumn(0).setCellRenderer(tcr);
       tablaEmpleado.getColumnModel().getColumn(1).setCellRenderer(tcr);
       tablaEmpleado.getColumnModel().getColumn(2).setCellRenderer(tcr);
       tablaEmpleado.getColumnModel().getColumn(3).setCellRenderer(tcr);
       tablaEmpleado.getColumnModel().getColumn(4).setCellRenderer(tcr);
       tablaEmpleado.getColumnModel().getColumn(5).setCellRenderer(tcr);
    }
    
    //PARA MOSTRAR EL CONTENIDO EN LA TABLA SEGUN EL BOTON SELECCIONADO
    private void agregaEnTabla(List <Empleado> listaEmpleado){
        
        DefaultTableModel modelo = (DefaultTableModel) tablaEmpleado.getModel();
        modelo.setRowCount(0);
        Object []emp=new Object[6];
        for (int i=0; i<listaEmpleado.size(); i++){
            Empleado empleado = listaEmpleado.get(i);
            
            emp[0] = empleado.getCodigo();
            emp[1] = empleado.getApellidoP()+ " " +empleado.getApellidoM()+ " " + empleado.getNombre();
            emp[2] = empleado.getSueldo();
            emp[3] = empleado.getSdi();
            emp[4] = empleado.getFehcaIngreso();
            emp[5] = empleado.getAntiguedad();
            
            modelo.addRow(emp);
        }
        
        alinear();
    }
