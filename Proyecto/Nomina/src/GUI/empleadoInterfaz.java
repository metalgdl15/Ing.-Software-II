    /**
     * AGREGA LO CAPTURADO EN EL GUI AL OBJETO EMPLEADO PARA MANDARLO A DAOMYSQL E INSERTAR
     */
    private void agregarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarEmpleadoActionPerformed
        Empleado empleado = new Empleado();
        
        //dao.setUsuario(getUsuario());
        if (txtNombre.getText().equals("") || txtApellidoP.getText().equals("") || txtApellidoM.getText().equals("")
                || txtSueldoD.getText().equals("") || txtFechaIngreso.getDate() == null){
            
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
            
        }else{
            empleado.setNombre(txtNombre.getText());
            empleado.setApellidoP(txtApellidoP.getText());
            empleado.setApellidoM(txtApellidoM.getText());
            empleado.setSueldo(Double.parseDouble(txtSueldoD.getText()));
            empleado.setFehcaIngreso(txtFechaIngreso.getDate());
            dao.agrega(empleado);
            cargarComboBox();
        }
    }//GEN-LAST:event_agregarEmpleadoActionPerformed

    /**
     * AGREGA LO CAPTURADO EN EL GUI AL OBJETO EMPLEADO PARA MANDARLO A DAOMYSQL Y MODIFICAR
     */
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        Empleado empleado = (Empleado) empleadoBoxAct.getSelectedItem();
        Empleado empleadoActualiza =new Empleado();
        //dao.setUsuario(getUsuario());
        
        empleadoActualiza.setCodigo(empleado.getCodigo());
        empleadoActualiza.setNombre(txtNombreAct.getText());
        empleadoActualiza.setApellidoP(txtApellidoPAct.getText());
        empleadoActualiza.setApellidoM(txtApellidoMAct.getText());
        
        //POR EL PROBLEMA DE CONVERTIR EL TEXT EN DOUBLE SI ES QUE ESTE CAMPO NO SE LLENO
        if("".equals(txtSueldoAct.getText())){
            empleadoActualiza.setSueldo(0.0);
        }else{
            empleadoActualiza.setSueldo(Double.parseDouble(txtSueldoAct.getText()));
        }
        
        dao.Actualiza(empleadoActualiza);
        cargarComboBox();
    }//GEN-LAST:event_btnActualizarActionPerformed

