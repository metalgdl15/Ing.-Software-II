    private void btnSeleccionadoConActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionadoConActionPerformed
        
        dao.setUsuario(getUsuario());
        List <Empleado> listaEmpleado = null;
        
        if(opc_1.isSelected()){
            Empleado empleadoCon = (Empleado) empleadoBoxCon.getSelectedItem();
            int codigo = empleadoCon.getCodigo();
            
            listaEmpleado = dao.obtenEmpleado(codigo);
        }else if (opc_2.isSelected()){
            Double sueldo = Double.parseDouble(txtConsulta.getText());
            listaEmpleado = dao.obtenMayorSueldo(sueldo);
            
        }else if (opc_3.isSelected()){
            Double sueldo = Double.parseDouble(txtConsulta.getText());
            listaEmpleado = dao.obtenMenorSueldo(sueldo);
        }
        
        agregaEnTabla(listaEmpleado);
    }//GEN-LAST:event_btnSeleccionadoConActionPerformed
    
